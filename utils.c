#include "philosophers.h"

bool ft_is_digit(char ch)
{
	return (ch >= '0' && ch <= '9');
}

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
		i++;
	return (i);
}
